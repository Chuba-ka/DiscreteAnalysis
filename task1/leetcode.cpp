#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
    int minNumberOfFrogs(string croakOfFrogs)
    {

        // =============================================
        // ЧАСТЬ 1: Линейная сортировка (подсчёт букв)
        // =============================================

        vector<int> count(26, 0); // массив из 26 нулей (для всех букв английского алфавита)

        // Проходим по всей строке один раз и считаем, сколько раз встретилась каждая буква
        for (char ch : croakOfFrogs)
        {
            count[ch - 'a']++; // 'c' - 'a' = 2, 'r'-'a'=17 и т.д.
        }

        // Проверяем самое важное правило:
        // Количество c, r, o, a, k ДОЛЖНО быть ОДИНАКОВЫМ
        // Если нет — значит какая-то лягушка не закончила "croak" → невозможно
        int total = count['c' - 'a']; // сколько должно быть полных "croak"

        if (count['c' - 'a'] == 0)
            return 0; // пустая строка или нет ни одной c

        if (count['c' - 'a'] != count['r' - 'a'] ||
            count['c' - 'a'] != count['o' - 'a'] ||
            count['c' - 'a'] != count['a' - 'a'] ||
            count['c' - 'a'] != count['k' - 'a'])
        {
            return -1;
        }

        // =============================================
        // ЧАСТЬ 2: Основная логика — симуляция крика лягушек
        // =============================================

        // Эти переменные показывают, сколько лягушек дошли до определённой буквы:
        int c = 0, r = 0, o = 0, a = 0, k = 0;

        int active = 0;   // СКОЛЬКО ЛЯГУШЕК СЕЙЧАС КРИЧАТ (начали, но не закончили)
        int maxFrogs = 0; // Максимальное количество одновременно кричащих лягушек

        // Проходим по строке ЕЩЁ РАЗ (второй проход)
        for (char ch : croakOfFrogs)
        {

            if (ch == 'c')
            {
                c++;      // Одна лягушка начала новое "croak"
                active++; // Теперь на одну активную лягушку больше
                if (active > maxFrogs)
                {
                    maxFrogs = active; // Запоминаем максимум
                }
            }
            else if (ch == 'r')
            {
                // Чтобы сказать 'r', должна существовать лягушка, которая уже сказала 'c',
                // но ещё не сказала 'r'
                if (r + 1 > c)
                    return -1; // Нарушение порядка!
                r++;
            }
            else if (ch == 'o')
            {
                if (o + 1 > r)
                    return -1;
                o++;
            }
            else if (ch == 'a')
            {
                if (a + 1 > o)
                    return -1;
                a++;
            }
            else if (ch == 'k')
            {
                if (k + 1 > a)
                    return -1;
                k++;
                active--; // Эта лягушка ЗАКОНЧИЛА свой "croak"
            }
        }

        // =============================================
        // ЧАСТЬ 3: Финальная проверка
        // =============================================

        // Если в конце все счётчики равны — значит всё правильно распределилось
        if (c == r && r == o && o == a && a == k)
        {
            return maxFrogs; // ← Это и есть ответ!
        }

        return -1; // На всякий случай, хотя при равных частотах обычно не доходит
    }
};